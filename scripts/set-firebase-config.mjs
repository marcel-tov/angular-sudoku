import {existsSync, readFileSync, writeFileSync} from 'fs';
import {resolve, dirname} from 'path';
import {fileURLToPath} from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, '..');

// --- Optional: load a local .env file (dependency-free) ---------------------
// Netlify (and other CI) inject env vars into process.env automatically, so
// this is only used for local development convenience.
const dotenvPath = resolve(root, '.env');

if (existsSync(dotenvPath)) {
    const content = readFileSync(dotenvPath, 'utf8');

    for (const rawLine of content.split(/\r?\n/)) {
        const line = rawLine.trim();

        if (!line || line.startsWith('#')) {
            continue;
        }

        const eq = line.indexOf('=');

        if (eq === -1) {
            continue;
        }

        const key = line.slice(0, eq).trim();
        let value = line.slice(eq + 1).trim();

        // Strip surrounding quotes if present
        if ((value.startsWith('"') && value.endsWith('"')) ||
            (value.startsWith("'") && value.endsWith("'"))) {
            value = value.slice(1, -1);
        }

        if (!(key in process.env)) {
            process.env[key] = value;
        }
    }
}

// --- Read + validate expected env vars --------------------------------------
const envMap = {
    apiKey: 'FIREBASE_API_KEY',
    authDomain: 'FIREBASE_AUTH_DOMAIN',
    projectId: 'FIREBASE_PROJECT_ID',
    storageBucket: 'FIREBASE_STORAGE_BUCKET',
    messagingSenderId: 'FIREBASE_MESSAGING_SENDER_ID',
    appId: 'FIREBASE_APP_ID',
};

const config = {};
const missing = [];

for (const [optionKey, envKey] of Object.entries(envMap)) {
    const value = process.env[envKey] ?? '';

    if (!value) {
        missing.push(envKey);
    }

    config[optionKey] = value;
}

if (missing.length > 0) {
    console.warn(
        '\n⚠ Building without Firebase configuration — the scan feature will\n' +
        '  be disabled at runtime. Missing env vars:\n  - ' +
        missing.join('\n  - ') +
        '\n\n  Set them in a local .env file or in\n' +
        '  Netlify → Site settings → Environment variables to enable scanning.\n',
    );
}

// --- Write firebase.config.ts -----------------------------------------------
const target = resolve(root, 'src/app/core/scan-dialog/firebase.config.ts');

const body = [
    '// AUTO-GENERATED by scripts/set-firebase-config.mjs — DO NOT EDIT.',
    '// Values come from environment variables at build time.',
    "import {FirebaseOptions} from 'firebase/app';",
    '',
    'const firebaseConfig: FirebaseOptions = {',
    `    apiKey: '${config.apiKey}',`,
    `    authDomain: '${config.authDomain}',`,
    `    projectId: '${config.projectId}',`,
    `    storageBucket: '${config.storageBucket}',`,
    `    messagingSenderId: '${config.messagingSenderId}',`,
    `    appId: '${config.appId}',`,
    '};',
    '',
    'export {firebaseConfig};',
    '',
].join('\n');

writeFileSync(target, body);
console.log(
    missing.length > 0
        ? '✔ wrote firebase.config.ts with empty values (scan feature disabled)'
        : `✔ wrote firebase.config.ts for project "${config.projectId}"`,
);
